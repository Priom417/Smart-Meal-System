public class Member {
    String firstName;
    String lastName;
    String  userName;
    String password;
    String email;
    String phoneNumber;
    String working_activity;
    String gender;
    String member_id;

    public String getFirstName() {
        return firstName;
    }

    public String getLastName() {
        return lastName;
    }

    public String getUserName() {
        return userName;
    }

    public String getPassword() {
        return password;
    }

    public String getEmail() {
        return email;
    }

    public String getPhoneNumber() {
        return phoneNumber;
    }

    public String getWorking_activity() {
        return working_activity;
    }

    public String getGender() {
        return gender;
    }

    public String getMember_id() {
        return member_id;
    }
}
